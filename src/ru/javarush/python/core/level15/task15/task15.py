# Подсчет времени сортировки
import os
import math
import time

# Напиши программу, которая будет считать сколько будет длиться сортировка 1 млрд. чисел тремя алгоритмами.
# Сложность первого алгоритма 100*n*ln(n).
# Сложность второго алгоритма 10*n^2.
# Сложность третьего алгоритма n^3.
# Выведите три этих числа на экран.

n = 1000000000

# Рассчитываем время выполнения для каждого алгоритма
time_algo1 = 100 * n * math.log(n)
time_algo2 = 10 * (n ** 2)
time_algo3 = n ** 3

# Выводим результаты
print(f"Время выполнения первого алгоритма (100 * n * ln(n)): {time_algo1:.2e}")
print(f"Время выполнения второго алгоритма (10 * n^2): {time_algo2:.2e}")
print(f"Время выполнения третьего алгоритма (n^3): {time_algo3:.2e}")
