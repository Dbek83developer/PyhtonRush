# Подсчет времени работы
import math

# Напиши программу, которая будет считать сколько будет длиться поиск решения для 100 данных тремя алгоритмами.
# Сложность первого алгоритма 100*n^10.
# Сложность второго алгоритма 10*2^n.
# Сложность третьего алгоритма n! .
# Выведите три этих числа на экран.

n = 100

# Рассчитываем время выполнения для каждого алгоритма
time_algo1 = 100 * (n ** 10)
time_algo2 = 10 * (2 ** n)
time_algo3 = math.factorial(n)

# Выводим результаты
print(f"Время выполнения первого алгоритма (100*n^10): {time_algo1:.2e}")
print(f"Время выполнения второго алгоритма (10*2^n): {time_algo2:.2e}")
print(f"Время выполнения третьего алгоритма (n!): {time_algo3:.2e}")