# Напишите программу, которая конвертирует текущее время из часового пояса UTC в заданный пользователем часовой пояс.
# Программа должна:
# Получить текущее время в часовом поясе UTC.
# Запросить у пользователя смещение в часах относительно UTC.
# Создать объект часового пояса с заданным смещением.
# Конвертировать текущее время в заданный часовой пояс.
# Вывести текущее время в UTC и в заданном часовом поясе.

# Напишите тут ваш код
from datetime import datetime, timedelta

# Шаг 1: Получить текущее время в часовом поясе UTC
utc_now = datetime.utcnow()
print(f"Текущее время в UTC: {utc_now.strftime('%Y-%m-%d %H:%M:%S')}")

# Шаг 2: Запросить у пользователя смещение в часах относительно UTC
offset_hours = int(input("Введите смещение относительно UTC (в часах): "))

# Шаг 3: Создать объект смещения на основе введенных часов
offset = timedelta(hours=offset_hours)

# Шаг 4: Конвертировать текущее время в заданный часовой пояс
user_time = utc_now + offset

# Шаг 5: Вывести текущее время в UTC и в заданном часовом поясе
print(f"Текущее время в вашем часовом поясе (UTC {offset_hours:+d}): {user_time.strftime('%Y-%m-%d %H:%M:%S')}")
